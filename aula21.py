# operadores: in (entrar)/ not in (não entrar)
# Strings são iteráveis
#  0 1 2 3 4 5
#  o t á v i o
# -6-5-4-3-2-1

nome = "Otávio"
print(nome [2]) #o python irá buscar a letra da carreira 2
print(nome [-4])#o python irá buscar a letra da carreira -4
print(10 * "-")
print("vio" in nome)#a palavra vio está no nome Otávio? True
print("zero" in nome)#a palavra zero está no nome Otávio? False
print("t" in nome)#a palavra t está no nome Otávio? True
print(10 * "-")
print("vio" not in nome)#vio não está em nome?
print("zero" not in nome)#zero não está em nome?
